a = [1, 2, 3, 4, 5, 6, 7, 8, 9]
console.log(a)
a.splice(1) ////这个会删除从2之后的所有数据
console.log(a) //这个会删除从2之后的所有数据
console.log('============================================')
b = [...a, 2, 3, 4, 5, 6, 7, 8, 9]
console.log(b)
b.splice(1, 1)
console.log(b) //只会删除指定位置的.

